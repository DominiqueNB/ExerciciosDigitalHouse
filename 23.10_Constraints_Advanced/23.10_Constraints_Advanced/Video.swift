import Foundation

class Video {
    
    var title: String
    var ratings: String
    var stars: String
    var description: String
    var image: String
    
    init(title: String, ratings: String, stars: String, description: String, image: String) {
        self.title = title
        self.ratings = ratings
        self.stars = stars
        self.description = description
        self.image = image
    }
    
}
