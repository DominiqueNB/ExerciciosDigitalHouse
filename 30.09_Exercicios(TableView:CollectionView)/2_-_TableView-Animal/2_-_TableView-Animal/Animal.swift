import Foundation

class Animal {
    var name: String
    var breed: String
    var weight: String
    var species: String
    
    init(name: String, breed: String, weight: String, species: String) {
        self.name = name
        self.breed = breed
        self.weight = weight
        self.species = species
    }
}
